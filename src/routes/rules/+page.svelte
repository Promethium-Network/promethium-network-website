                <h2 class="h2 underline">In-Game Rules</h2>
                <ol class="list-decimal p-4 space-y-8">
                    <li>
                        <strong>No</strong> client modifications that give an advantage over other players. The Vivecraft mod is usually excluded in this rule, except for some select cases.
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                The cases where the Vivecraft mod is not excluded is in the case of Vivecraft speed + fireworks to move faster than intended, or in the case of Vivecraft teleport to reach inaccessible locations, or movement inertia to go faster than normal players.
                            </li>
                            <li>
                                Autoclickers are <strong>not</strong> allowed in combat/pvp with other players, or in AFK farms.
                            </li>
                        </ul>
                    </li>
                    <li>
                        The use and exploitation of glitches or bugs is <strong>prohibited</strong>, and anyone found to be doing so will be punished.
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>Anyone who reports a bug after abusing it is still liable for punishment.</li>
                        </ul>
                    </li>
                    <li>
                        Players are <strong>prohibited</strong> from selling cosmetics, items, or any coordinates obtained in game for real world currency.
                    </li>
                    <li>
                        Threats against the server’s stability, most commonly in the forms of DDoSing or backdoors, are <strong>not</strong> allowed and will be punished very harshly.
                    </li>
                    <li>
                        Items that are not intended for the public found on players will be confiscated, and the player may be punished.
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>This includes “staff items” such as barriers, command blocks, or unobtainable items such as player heads, bedrock, the debug stick, or light blocks.</li>
                        </ul>
                    </li>
                </ol>
