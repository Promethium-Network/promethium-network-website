                <h2 class="h2 underline">Network Rules</h2>
                <ol class="list-decimal p-4 space-y-8">
                    <li>
                        <strong>No</strong> spamming text chat or voice chat.
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                Use of soundboards, voice changers, and music through voice chat is prohibited.
                            </li>
                        </ul>
                    </li>
                    <li>
                        Advertisement of <strong>non</strong>-Promethium related content is <strong>prohibited</strong>.
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                This includes, but is not limited to, posting Minecraft server IPs or website links.
                            </li>
                            <li>
                                Advertisement of Promethium-related content is allowed in the #media channel in the Discord.
                            </li>
                        </ul>
                    </li>
                    <li>Excessive cursing, whether it is in text chat or voice chat, is <strong>not</strong> allowed.</li>
                    <li><strong>No</strong> asking staff members for special permissions, any items, or any cosmetics.</li>
                    <li>
                        Harassment of players, regardless of their background or beliefs, is <strong>prohibited</strong> and will be punished.
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                Harassment of players through criminal methods such as, but never limited to, doxxing or blackmailing will also not be tolerated, and will be punished very harshly.
                            </li>
                            <li>
                                Sexual harassment will <strong>NOT</strong> be tolerated what so ever.
                            </li>
                            <li>
                                Leaking information, such as player or base coordinates, <strong>in public chat</strong> is also not allowed. Private chats, such as private groups or servers, are exempt from this rule.
                            </li>
                        </ul>
                    </li>
                    <li>
                        Impersonation of players for malicious purposes is <strong>not</strong> allowed.
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>The context of this case can vary, and staff can deem whether the impersonation was used maliciously.</li>
                        </ul>
                    </li>
                    <li>
                        NSFW or offensive topics and content, both in the discord or in-game, is <strong>not</strong> allowed.
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>This includes NSFW item names or builds, offensive usernames or skins, or the use of any derogatory terms usually referred to as slurs.</li>
                        </ul>
                    </li>
                    <li>
                        Disregarding / <strong>ignoring a staff member is punishable</strong>, please be considerate to the staff team and listen to their requests / enforcements.
                    </li>
                    <li>
                        Saying you haven’t read the rules is <strong>NOT</strong> a valid excuse for breaking them.
