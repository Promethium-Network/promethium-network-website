            <div>
                <h2 class="h2 underline">Earth Rules</h2>
                <ol class="list-decimal p-4 space-y-8">
                    <li>
                        <strong>Griefing</strong>
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                Destroying land in or around claims
                                <ul class="list-disc px-4 py-2 space-y-4">
                                    <li>Players may not damage land in or around claims (griefing).</li>
                                </ul>
                                Players breaking this rule will be warned and damage will be rolled back. Players doing this more than once may face a permanent ban. Placing and removing blocks during siege war events is allowed when necessary to the extent needed in the conflict.
                            </li>
                        </ul>
                        Examples of grief (but not limited to):
                        <ul class="list-disc px-8 py-2 space-y-4">
                            <li>Cobble monsters – Mountain-like structures that are built using lava and water.</li>
                            <li>Lava/water curtains – Placing rows of lava/water in the sky to make a wall of falling lava.</li>
                            <li>TNT craters.</li>
                            <li>Mass spawning entities within or around a town (chickens, boats, etc.).</li>
                        </ul>
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                Destroying land in wilderness
                                <br/>
                                <br/>
                                Wilderness is defined by Towny as any land not currently claimed. You are allowed to grief structures or terrain as long as they are not within or in close proximity to a claimed area. However, any player may request major terrain grief to be rolled back even in the wilderness.
                                <br/>
                                <br/>
                                Breaking railways or ice roads is not allowed even in the wilderness, if willingly connecting two existing towns. This does not only apply to the rails, but all blocks the railroad is made of.
                            </li>
                        </ul>
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                Land claims
                                <br />
                                <br />
                                Claiming in a way to restrict the ability of neighboring towns to expand, referred to as claim blocking, is not allowed.
                                <br />
                                <br />
                                Creating claim arms extending from towns is not allowed. These are protrusions which are often used to block the settlement and expansion of other towns or just artificially expand the area a town controls without the need to claim all of it.
                                <br />
                                <br />
                                Towns are not allowed to have areas of unclaimed chunks inside of their borders, as this can substantially increase the size of a town without them actually having to increase their claim limit legitimately.
                                <br />
                                <br />
                                Towns which are clearly created for no other reason than with the intent to block an existing town will also be removed.
                            </li>
                        </ul>
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                Major terraforming
                                <br />
                                <br />
                                In order to preserve the accurate geography of the map, major terraforming of the natural terrain is discouraged. Examples of this include for instance draining large lakes/rivers, creating artificial islands which do not exist in real life, or covering the ocean with platforms. Vertical terraforming is allowed as it does not change the Dynmap view.
                            </li>
                        </ul>
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                Map Art
                                <br/>
                                <br/>
                                Creating map art is only allowed on the Earth world if you are given permission by a staff member (Senior Mod+). It is recommended to place map art in the End as there is a lot of unused space there. If you place it on the Earth map without permission from staff, it will be removed without warning, and you will receive zero compensation for the blocks placed.
                                <br/>
                                <br/>
                                Your map art may not contain nudity, sexual content, racist imagery, or anything else that breaks any part of the other rules.
                            </li>
                        </ul>
                    </li>
                    <li>
                        <strong>Land Claims</strong>
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                Snake Claims
                                <br />
                                <br />
                                Snake Claims, these are thin claims that create a line through the map. When creating your nation's outline, you will be making a snake claim as long as the claim is connected and filled it will not be seen as a snake claim.
                                <br />
                                <br />
                                Snake Claims are also used to create separated claims, these are only permitted if the snake claim is unclaimed once the separated claim is complete.
                            </li>
                        </ul>
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                Hollow Claims
                                <br />
                                <br />
                                Hollow Claims are when you create a large perimeter of claims then proceed to not fill in the perimeter. These claims are not allowed.
                            </li>
                        </ul>
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                Seperated Claims
                                <br />
                                <br />
                                Separated Claims are claims that are not directly touching the home claim block. There are exceptions to what is allowed for separated claims.
                                <br />
                                <br />
                                Ex. Claiming the Islands near Greece, you are allowed to claim these Islands as they are close to the main claim block if it is on the Greece mainland.
                                <br />
                                <br />
                                There will be many exceptions to these rules, depending on the distance between the home claim block and the separated claim. For the best answer to this rule please create either a ticket or ask a staff member.
                            </li>
                        </ul>
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                Ocean Claims
                                <br />
                                <br />
                                Claiming the ocean is not allowed. You are allowed to claim your coast if you have started to build on the coast. A good rule of thumb is 1-3 chunks in the water from your coast is allowed.
                            </li>
                        </ul>
                    </li>
                    <li>
                        <strong>Blocking Access to Warps</strong>
                        <ul class="list-disc px-4 py-2 space-y-4">
                            <li>
                                You are not allowed to build a wall, giant hole or any other structure that will obstruct anyone from being able to leave the warp area. If you make an obstruction it will be removed without warning and you will receive zero compensation.
                                <ul class="list-disc px-4 py-2 space-y-4">
                                    <li>If you continue to obstruct warps, you will be given a several day ban depending on how many previous punishments you have received for this offense.</li>
                                </ul>
                            </li>
                        </ul>
                    </li>
                </ol>
